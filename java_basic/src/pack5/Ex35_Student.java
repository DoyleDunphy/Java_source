package pack5;
// 레코드 형태의 클래스(집단 기억장소)로 자료 전달용 ( DTO : Data Transfer Object )
public class Ex35_Student {
	private String hakbun, irum;
	private int jumsu;
	
	public String getHakbun() {
		return hakbun;
	}
	public void setHakbun(String hakbun) {
		this.hakbun = hakbun;
	}
	public String getIrum() {
		return irum;
	}
	public void setIrum(String irum) {
		this.irum = irum;
	}
	public int getJumsu() {
		return jumsu;
	}
	public void setJumsu(int jumsu) {
		this.jumsu = jumsu;
	}
	
	
}
